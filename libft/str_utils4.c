#include "libft.h"

char	*free_strdup(char *str, char **freestr)
{
	char	*tmp;

	tmp = ft_strdup(str);
	free(*freestr);
	return (tmp);
}

char	*ft_skipstring(int offset, char *str)
{
	char	*dst;

	dst = ft_strdup(str + offset);
	return (free(str), dst);
}

char	*join_n_char(char *s1, char c, int n)
{
	char	*dst;
	int		i;

	dst = malloc(ft_strlen(s1) + n + 1);
	if (!dst)
		return (NULL);
	i = 0;
	while (s1[i])
	{
		dst[i] = s1[i];
		i++;
	}
	while (n--)
		dst[i++] = c;
	dst[i] = '\0';
	free(s1);
	return (dst);
}
